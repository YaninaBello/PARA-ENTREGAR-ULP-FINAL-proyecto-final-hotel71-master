    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        try {

            Integer cantPersonas = Integer.parseInt(jcBoxCantPersonas.getSelectedItem().toString());
            Integer cantCamas = Integer.parseInt(jcBoxCantCamas.getSelectedItem().toString());
            String tipoCamas = jcBoxTipoCamas.getSelectedItem().toString();
            String tipoDeHabitacion = jcBoxTipoHabitacion.getSelectedItem().toString();
            Integer precio = Integer.parseInt(jtPrecio.getText().toString());
            Boolean estado = jrbEstado.isSelected();
            if (tipoDeHabitacionNueva == null) {

                tipoDeHabitacionNueva = new TipoDeHabitacion(cantPersonas, cantCamas, tipoCamas, tipoDeHabitacion, precio, estado);
                thData.altaTipoHabitacion(tipoDeHabitacionNueva);

            } else {

                tipoDeHabitacionNueva.setCantPersonas(cantPersonas);
                tipoDeHabitacionNueva.setCantCamas(cantCamas);
                tipoDeHabitacionNueva.setTipoCamas(tipoCamas);
                tipoDeHabitacionNueva.setTipoDeHabitacion(tipoDeHabitacion);
                thData.altaTipoHabitacion(tipoDeHabitacionNueva);

            }

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this, "Debe ingresar un numero valido");
        }

        limpiarCampos();
    }//GEN-LAST:event_jBGuardarActionPerformed

