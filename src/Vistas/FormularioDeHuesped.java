    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:

        try {
            Integer dni = Integer.parseInt(jtDni.getText()); //obtenemos el dni
            huespedActual = hueData.buscarHuespedPorDni(dni);
            if (huespedActual != null) {

                jtNombre.setText(huespedActual.getNombre());
                jtApellido.setText(huespedActual.getApellido());
                jtDomicilio.setText(huespedActual.getDomicilio());
                jtCorreo.setText(huespedActual.getCorreo());
                jtCelular.setText(Integer.toString(huespedActual.getCelular()));

                jrbEstado.setSelected(huespedActual.isActivo());

            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero valido");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jtDniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtDniKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();

        boolean numero = key >= 48 && key <= 57;

        if (!numero) {
            evt.consume();
        }
    }//GEN-LAST:event_jtDniKeyTyped

