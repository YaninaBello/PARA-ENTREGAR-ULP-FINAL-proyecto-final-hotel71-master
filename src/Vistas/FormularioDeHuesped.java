    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:

        try {
            Integer dni = Integer.parseInt(jtDni.getText());
            String nombre = jtNombre.getText();
            String apellido = jtApellido.getText();
            String domicilio = jtDomicilio.getText();
            String correo = jtCorreo.getText();
            Integer celular = Integer.parseInt(jtCelular.getText());
            if (nombre.isEmpty() || apellido.isEmpty() || domicilio.isEmpty() || correo.isEmpty()) {

                JOptionPane.showMessageDialog(this, "No puede haber campos vacios");
                return;
            }

            Boolean estado = jrbEstado.isSelected();
            if (huespedActual == null) {

                huespedActual = new Huesped(nombre, apellido, dni, domicilio, correo, celular, estado);
                hueData.altaHuesped(huespedActual);

            } else {

                huespedActual.setDni(dni);
                huespedActual.setNombre(nombre);
                huespedActual.setApellido(apellido);
                huespedActual.setDomicilio(domicilio);
                huespedActual.setCorreo(correo);
                huespedActual.setCelular(celular);
                hueData.modificarHuesped(huespedActual);

            }

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this, "Debe ingresar un DNI valido");
        }

    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:

        if (huespedActual != null) {

            hueData.bajaHuesped(huespedActual.getIdHuesped());
            huespedActual = null;
            limpiarCampos();

        } else {

            JOptionPane.showMessageDialog(this, "No hay un huesped seleccionado");
        }

    }//GEN-LAST:event_jbEliminarActionPerformed

